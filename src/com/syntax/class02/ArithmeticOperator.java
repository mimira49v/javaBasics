package com.syntax.class02;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
		// =, -, /, %
		
		int num1 = 10;
		int num2 = 20;
		//int round number
		System.out.println(num1+num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		
		//double shows decimals can hold up to 15-16 decimals
		double num3 = 10;
		double num4 = 20;
		
		System.out.println(num3/num4);
		
		//float holds 5-6 decimals
		float num5 = 10f;
		float num6 = 3f;
		
		System.out.println(num5/num6);
	
		// % - remainder
		
		int j=10;
		int i=6;
		System.out.println(j%i);
		//ACTION ON THE RIGHT HAPPENS FIRST*
		int n1=20;
		int n2=5;
		int sub=n1-n2;
		int sum=(n1+n2);
		int div=n1/n2;
		
		
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(div);
		
		double d1=20;
		double d2=3;
		
		double division=d1/d2;
		System.out.println(division);
		
		double q1=100.25;
		double q2=20.20;
		double q3=3.9;
		
		
		
		
	}

}
